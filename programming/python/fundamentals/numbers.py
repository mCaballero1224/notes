#!/usr/bin/python

""" INTEGERS

    Integers are whole numbers, including positive and negative values
    along with 0. Standard operators (+, -, /, *) can be used to form
    mathmatical operations to integers.
"""

i = 20
i = 20 + 1
print(i)
i = 20 * 3
print(i)
i = 20 / 4
print(i)
i = 20 - 3
print(i)

# Order of operations can be modified via parentheses
i = 20 / (10+10)

""" FLOATS

    Floats are numbers with a decimal point value. The float describes how
    the decimal point can appear at any position in a number.

    Floats generally act in a similar manner like integers.
"""

i = 0.5 + 0.5
print(i)
i = i * 5.0
print(i)
i = i / 2.0
print(i)
i = i - 2.0
print(i)
