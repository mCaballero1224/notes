#!/usr/bin/python

"""
    Variables are labels that you can assign a value to. Variables are
    ALWAYS associated iwth a value.

    To declare a value, use the following syntax:

        variable_name = value

    Variable names can only contain letters, numbers, and underscores. But can
    only start with a letter or an underscore.
"""


def main():
    # To declare a value, use the following syntax: variable_name = value
    # Variable names can only contain letters, numbers, and underscores
    #
    message = 'Hello, world!'
    print(message)
    message = 'Good Bye!'  # you can reassign the value of a variable
    print(message)
